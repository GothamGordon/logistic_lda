import re
import string

from nltk import WordNetLemmatizer
from nltk import pos_tag
from nltk import sent_tokenize
from nltk import wordpunct_tokenize
from nltk.corpus import stopwords as sw
from nltk.corpus import wordnet as wn


class NLTKPreprocessor():
    def __init__(self, lower=True, strip=True):
        self.lower = lower
        self.strip = strip
        self.stopwords = set(sw.words('english')).union(SKLEARN_STOP_WORDS)
        self.punct = set(string.punctuation)
        self.lemmatizer = WordNetLemmatizer()

    def tokenize(self, document, remove_stop_words=True):
        tokens = []
        for sent in sent_tokenize(document):
            for token, tag in pos_tag(wordpunct_tokenize(sent)):
                token = token.lower() if self.lower else token
                token = token.strip() if self.strip else token
                token = token.strip('_') if self.strip else token
                token = token.strip('*') if self.strip else token
                token = re.sub('[0-9]+', '', token)

                # If stopword, ignore token and continue
                if remove_stop_words and token in self.stopwords:
                    continue

                if len(token) < 2:
                    continue

                if str.isdigit(token):
                    continue

                # If punctuation, ignore token and continue
                if all(char in self.punct for char in token):
                    continue

                # Lemmatize the token and yield
                lemma = self.lemmatize(token, tag)
                tokens.append(lemma)
        return tokens

    def lemmatize(self, token, tag):
        tag = {
            'N': wn.NOUN,
            'V': wn.VERB,
            'R': wn.ADV,
            'J': wn.ADJ
        }.get(tag[0], wn.NOUN)

        return self.lemmatizer.lemmatize(token, tag)


